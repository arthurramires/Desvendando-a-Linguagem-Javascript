//Aula 2 - Number

var nota = 10;

//Eleva o valor ao exponente escolhido
nota.toExponencial(2);//100
//Retorna o valor com o numero de casas decimais escolhido
nota.toFixed(2);//10.00
nota.toPrecision(1);//1e+1
//Transforma o valor em uma String
nota.toString();//"10"
//Retorna o valor presente na variavel
nota.valueOf();//10
